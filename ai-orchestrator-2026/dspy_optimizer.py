"""
dspy_optimizer.py — Оптимизация промптов (DSPy MIPROv2 + LLM fallback)
========================================================================
Два режима оптимизации:
  1. DSPy MIPROv2 — если dspy установлен и trainset >= 3
     - Trainset читается из ChromaDB (одобрённые промпты)
     - Compiled program кешируется на диске
  2. LLM-based fallback — прямой вызов LLM для улучшения промпта
"""

