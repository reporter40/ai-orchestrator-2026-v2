# =============================================================================
# DSPy Signature
# =============================================================================

if _DSPY_AVAILABLE:

    class PromptOptimizationSignature(dspy.Signature):
        """Optimize a prompt draft to maximize task fulfillment, factual accuracy, and minimize hallucination risk. Use few-shot examples as reference for quality bar."""

        original_request: str = dspy.InputField(
            desc="The original user request that the prompt must fulfill"
        )
        previous_draft: str = dspy.InputField(
            desc="The previous prompt draft that scored below threshold"
        )
        previous_score: float = dspy.InputField(
            desc="S_final score of the previous draft (0.0-1.0)"
        )
        weakness_hint: str = dspy.InputField(
            desc="What aspect scored lowest: task_fulfillment, rag_accuracy, or hallucination"
        )
        optimized_prompt: str = dspy.OutputField(
            desc="Improved prompt that addresses the weaknesses. Must be specific, structured, and hallucination-resistant."
        )


    class InstructionOptimizationSignature(dspy.Signature):
        """Improve an AI agent's system instructions based on past failure analysis to prevent similar issues in future iterations."""

        original_request: str = dspy.InputField(desc="The task the agent failed to fulfill perfectly")
        agent_role: str = dspy.InputField(desc="The role of the agent being optimized")
        failed_prompt: str = dspy.InputField(desc="The prompt draft generated by the agent")
        failure_analysis: str = dspy.InputField(desc="Technical analysis of why the prompt was suboptimal")
        current_instruction: str = dspy.InputField(desc="The agent's current system instruction")
        improved_instruction: str = dspy.OutputField(
            desc="Refined system instruction that incorporates the lessons learned. Keep it concise but more effective."
        )


# =============================================================================
# Metric function for MIPROv2
# =============================================================================

def prompt_quality_metric(example, prediction, trace=None) -> float:
    """
    Метрика качества оптимизированного промпта для MIPROv2.
    Оценивает: длину, наличие ключевых признаков, улучшение.
    """
    score = 0.0
    prompt_text = getattr(prediction, "optimized_prompt", "")

    if len(prompt_text) > 50:
        score += 0.3

    keywords = ["role", "task", "output", "format", "instruction", "step", "example"]
    if any(kw in prompt_text.lower() for kw in keywords):
        score += 0.4

    prev_score = getattr(example, "previous_score", 0.85)
    prev_draft = getattr(example, "previous_draft", "")
    if prev_score < 0.85 and len(prompt_text) > len(prev_draft):
        score += 0.3

    return min(1.0, score)


